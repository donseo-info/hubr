    /**
     * @param string $service
     * @param int    $user_id
     *
     * @return string|null
     */
    public function get_url( $service, $user_id = null ) {
        if ( $user_id ) {
            $url = get_user_meta( $user_id, $service, true );
        } else {
            $url = $this->get_url_from_customizer( $service );
        }

        /**
         * @since 1.0
         */
        $url = apply_filters( 'wpcommunity/social/profile_link', $url, $service, $user_id );

        return $url;
    }

    /**
     * @param string $service
     *
     * @return string|null
     */
    protected function get_url_from_customizer( $service ) {
        if ( null === $this->_customizer_profile_stash ) {
            $this->_customizer_profile_stash = [];

            $profile_data = CustomizerHelper::get_json_option_values( 'social_profiles.profiles' );
            // looking url for desktop only
            if ( isset( $profile_data['desktop'] ) ) {
                foreach ( $profile_data['desktop'] as $profile_datum ) {
                    $this->_customizer_profile_stash[ $profile_datum['name'] ] = $profile_datum;
                }
            }
        }

        return $this->_customizer_profile_stash[ $service ]['url'] ?? null;
    }

    /**
     * @return void
     */
    protected function setup_default_hooks() {
        add_filter( 'wpcommunity/social/profile_link', [ $this, '_set_social_profile_link' ], 10, 2 );
    }

    /**
     * @param string $url
     * @param string $service
     *
     * @return string
     */
    public function _set_social_profile_link( $url, $service ) {
        if ( $url ) {
            switch ( $service ) {
                case 'whatsapp':
                    $url = 'https://api.whatsapp.com/send?phone=' . $url;
                    break;
                case 'viber':
                    if ( wp_is_mobile() ) {
                        $url = 'viber://chat?number=' . $url;
                    } else {
                        $url = str_replace( '+', '', $url );
                        $url = 'viber://add?number=' . $url;
                    }
                    break;
                default:
                    break;
            }
        }

        return $url;
    }
