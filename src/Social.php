    /**
     * @param array $buttons
     * @param array $args
     *
     * @return string
     */
    public function get_share_buttons( array $buttons, array $args = [] ) {
        $args = wp_parse_args( $args, [
            'show_label'          => $this->customizer->get_option( 'social_share.show_text' ),
            'show_counters'       => $this->customizer->get_option( 'social_share.show_counters' ),
            'use_original_colors' => $this->customizer->get_option( 'social_share.use_original_colors' ),
            'show_all_data'       => false,
        ] );

        $render_share_button = function ( $button, $service, $args ) {
            $data = [
                'social' => $button,
            ];

            if ( in_array( $button, [ 'vkontakte', 'moimir', 'wordpress' ] ) ) {

                /**
                 * Allows to change share image size
                 */
                $image_size = apply_filters( 'wpcommunity/social/share_image_size', 'thumbnail' );

                $image_id = get_post_thumbnail_id();
                if ( $image_url = wp_get_attachment_image_src( $image_id, $image_size ) ) {
                    $data['image'] = ! empty( $image_url[0] ) ? $image_url[0] : '';
                }
            }

            if ( $button == 'pinterest' || $args['show_all_data'] ) {
                $data['url']         = get_permalink();
                $data['title']       = get_the_title();
                $data['description'] = get_the_excerpt();
            }

            $attributes = [];
            foreach ( $data as $key => $value ) {
                $attributes[ 'data-' . $key ] = $value;
            }
            $attributes['classes'] = [
                'social-button',
                'social-button--' . $button,
            ];
            if ( $args['use_original_colors'] && ! empty( $service['color'] ) ) {
                $attributes['style'] = esc_attr( '--wpsc-social-button-bg:' . $service['color'] );
            }


            $result = '<span ' . get_the_attributes( 'render_share_button', $attributes, compact( 'button', 'service', 'args' ) ) . '>';
            if ( ! empty( $service['svg'] ) ) {
                $result .= '<span class="social-button__icon">' . $service['svg'] . '</span>';
            } elseif ( ! empty( $service['path'] ) ) {
                $result .= '<span class="social-button__icon">';
                $result .= '<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ' . esc_attr( $service['width'] ?? '32' ) . ' ' . esc_attr( $service['height'] ?? '32' ) . ' ">';
                $result .= '<path d="' . esc_attr( $service['path'] ) . '" fill="currentColor"/>';
                $result .= '</svg>';
                $result .= '</span>';
            }

            if ( $args['show_label'] ) {
                $result .= '<span class="social-button__label">' . ( $service['label'] ?? '' ) . '</span>';
            }
            if ( $args['show_counters'] && ! empty( $service['counter'] ) ) {
                $result .= '<span class="social-button__counter" data-counter="' . $button . '"></span>';
            }

            $result .= '</span>';

            return $result;
        };

        $html           = '';
        $share_services = $this->get_share_services();

        foreach ( $buttons as $button ) {
            if ( ! array_key_exists( $button, $share_services ) ) {
                continue;
            }

            $service = $share_services[ $button ];

            /**
             * Allows to change share button render functions
             */
            $render_share_button = apply_filters( 'wpcommunity/social/render_share_button', $render_share_button, $button, $service );

            if ( is_callable( $render_share_button ) ) {
                $html .= $render_share_button( $button, $service, $args ) . PHP_EOL;
            }

        }

        return $html;
    }

